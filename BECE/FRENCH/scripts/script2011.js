            let currentQuiz = 0;
            let score = 0;
        
            const deselectAnswers = () => {
                answerElements.forEach((answer) => (answer.checked = false));
                };
                const getSelected = () => {
                let answer;
                answerElements.forEach((answerElement) => {
                if (answerElement.checked) answer = answerElement.id;
                });
                return answer;
                };
